    private void jButton1_cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1_cadastrarActionPerformed
        try {//aqui verifico se todos os campos estão vazios, caso estejam, solicito que sejam preenchidos
            if (jTextField1_nome.getText().isEmpty()
                    || jTextField2_carga_horaria.getText().isEmpty()
                    || jTextField3_curso.getText().isEmpty()
                    || jTextField4_numero_vagas.getText().isEmpty()
                    || jTextField5_periodo.getText().isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "Ops! Preencha todos os campos!", "AVISO!", JOptionPane.ERROR_MESSAGE);
            } else {
                //insert dos dados no banco
                String cmd = "insert into disciplina (nome, carga_horaria, curso, numero_vagas, periodo) VALUES " + "('"
                        + jTextField1_nome.getText() + "', " + ""
                        + jTextField2_carga_horaria.getText() + ", '"
                        + jTextField3_curso.getText() + "', "
                        + jTextField4_numero_vagas.getText() + ", '"
                        + jTextField5_periodo.getText() + "')";

                String mensagem = "Dados inseridos com sucesso!"; //exibindo mensagem de confirmação de dados inseridos no bd
                JOptionPane.showMessageDialog(rootPane, mensagem, "AVISO!", JOptionPane.INFORMATION_MESSAGE);
                //chama o metodo que limpa todos os campos de texto logo após o cadastro
                acaobotaolimpa();
                
                Connection con = ConexaoMySQL.getConexaoMySQL();
                //String cmd = null;
                con.createStatement().executeUpdate(cmd);
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton1_cadastrarActionPerformed

