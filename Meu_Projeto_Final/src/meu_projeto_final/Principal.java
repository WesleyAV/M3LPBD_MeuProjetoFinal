    private void carregarTabela() {
        try {
            Connection con = ConexaoMySQL.getConexaoMySQL();//conexão utilizando a classe ConexaoMySQL
            
            String cmd = "select * from disciplina";//string que recebe o resultado do select no banco
            
            ResultSet res = con.createStatement().executeQuery(cmd);//resultado do select armazenado no variável res
            
            while (res.next()) {//laço de repetição que pega os campos digitados
                String nomeDB = res.getString("nome");
                int idDB = res.getInt("id");
                int carga_horariaDB = res.getInt("carga_horaria");
                String cursoDB = res.getString("curso");
                int numero_vagasDB = res.getInt("numero_vagas");
                String periodoDB = res.getString("periodo");

                Vector v = new Vector();//vetor que armazena os dados

                v.add(idDB);
                v.add(nomeDB);
                v.add(carga_horariaDB);
                v.add(cursoDB);
                v.add(numero_vagasDB);
                v.add(periodoDB);

                ((DefaultTableModel) jTable1.getModel()).addRow(v);//pega os dados da tabela e adiciona em uma linha
            }

        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }

    private void carregarCombo() {
        //ResultSet res = null;
        try {
            Connection con = ConexaoMySQL.getConexaoMySQL();
            String cmd = "select nome from disciplina";
            ResultSet res = con.createStatement().executeQuery(cmd);
            while (res.next()) {
                String nomeDB = res.getString("nome");//atribui o nome da disciplina que veio do select no banco

                ((DefaultComboBoxModel) jComboBox1.getModel()).addElement(nomeDB);

            }

        } catch (SQLException ex) {
            ex.printStackTrace();
        }

    }

